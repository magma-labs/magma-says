fn print_border(repeater: char, len: usize) {
    let border = repeat(repeater).take(len).collect::<String>();
    println!("{}", border);
}

pub fn say(message: &str) {
    print_border('-', message.len());
    println!("{}", message);
    print_border('-', message.len());
    println!("{}", MAGMA);
}
